SELECT *
FROM EMP
WHERE ENAME LIKE 'A%';

SELECT *
FROM EMP
WHERE MGR_ID IS NULL;

SELECT ENAME, EMPNO, SAL
FROM EMP
WHERE SAL BETWEEN 1200 AND 1400;

-- Before the pay rise
SELECT *
FROM EMP
WHERE DEPTNO = 20;

-- Apply the pay rise
UPDATE EMP
SET SAL = SAL * 1.10
WHERE DEPTNO = 20;

-- After the pay rise
SELECT *
FROM EMP
WHERE DEPTNO = 20;

SELECT COUNT(*) AS "Number of Clerks"
FROM EMP
WHERE JOB = 'CLERK';

SELECT JOB, AVG(SAL) AS "Average Salary", COUNT(*) AS "Number of Employees"
FROM EMP
GROUP BY JOB;

SELECT *
FROM EMP
WHERE SAL IN (SELECT MIN(SAL) FROM EMP UNION SELECT MAX(SAL) FROM EMP);

SELECT *
FROM DEPT
WHERE DEPTNO NOT IN (SELECT DISTINCT DEPTNO FROM EMP);

SELECT ENAME, SAL
FROM EMP
WHERE JOB = 'ANALYST' AND SAL > 1200 AND DEPTNO = 20
ORDER BY ENAME ASC;

SELECT D.DNAME, D.DEPTNO, SUM(E.SAL) AS "Total Salary"
FROM DEPT D
LEFT JOIN EMP E ON D.DEPTNO = E.DEPTNO
GROUP BY D.DNAME, D.DEPTNO;

SELECT ENAME, SAL
